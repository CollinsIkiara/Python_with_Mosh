# String Methods in Python

course = "Python for Beginners"
print(len(course))
print(course)
print(course.upper())
print(course.lower())
print(course.title()) # Converts the first character of each word to uppercase
print(course.find("Beginners")) # Returns the index of the first occurrence of the specified substring.
print(course.replace("Beginners", "Absolute Beginners"))
print("Python" in course) # The 'in' operator checks if a substring exists within a string and returns True or False.