# In Python, we have the following arithmetic operators: +, -, *, /, //, %, **, representing addition, subtraction, multiplication, division, floor division, modulus, and exponentiation respectively. We also have augmented assignment operators such as +=, -=, *=, /=, //=, %=, **= which are used to perform an operation and assign the result back to the variable in a single step.

# The following below code shows an example of augmented assignment operator for subtraction.

x = 10
x -= 3
print(x)

# The following example demonstrates operator precedence in Python. The order of operations is determined by the precedence of the operators, which can be remembered using the acronym PEMDAS (Parentheses, Exponents, Multiplication and Division, Addition and Subtraction).

x = (2 + 3) * 10 - 3
print(x)