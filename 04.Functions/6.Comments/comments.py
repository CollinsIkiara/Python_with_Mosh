# This is a comment in Python. Comments are ignored by the interpreter and are used to explain code.

# prints Sky is blue
print("Sky is blue")

# prints message to the console
print("Ocean is blue")

# Important notes on comments:
# 1. Comments can be single-line or multi-line.
# 2. Single-line comments start with a hash symbol (#).
# 3. Multi-line comments can be created using triple quotes (''' or """).
# 4. Comments shouldn't explain the obvious, (e.g., like the examples above) but should provide insight into why and how the code is doing something, not what it is doing.
# 5. Comments should be written in such a way that if the code changes, there's no need to change the comment. This means that comments should focus on the intent and purpose of the code rather than describing the code itself, i.e., in the first example above, if the print statement changes to print("Sky is green"), the comment would be outdated and misleading.